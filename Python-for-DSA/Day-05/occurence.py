#Write a program to count how many times a specific number appears in a list.



numbers = [1, 2, 3, 4, 2, 5, 2] #solution

# Input the number to count
num_to_count = 2

# Count occurrences
count = numbers.count(num_to_count)

# Print result
print(f"The number {num_to_count} appears {count} times in the list.")
